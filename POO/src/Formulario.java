    public void agregar(){
        Contacto c= new Contacto(TextId.getText(),TextNombre.getText(),TextApellido.getText());
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int respuesta = fc.showOpenDialog(this);
        if (respuesta == JFileChooser.APPROVE_OPTION) {
            File directorioElegido = fc.getSelectedFile();
            String nombre = directorioElegido.getAbsolutePath();
            try {
                String contenido = "Nombre = " + c.getNombre() + "\n" +
                "Apellidos = " + c.getApellidos() + "\n" + "Identificacion = " +
                c.getIdentificacion();
                String nombreFile= nombre+"\\"+c.getIdentificacion()+".txt";
                id.add((String)c.getIdentificacion());
                rut.add((String)nombreFile);
                File file = new File(nombreFile);
                PrintWriter salida= new PrintWriter(new FileWriter(file,true));
                salida.println(contenido);
                salida.close();
                String texto = "Proceso exitoso ";
                JOptionPane.showMessageDialog(this, texto,"Mensaje",JOptionPane.INFORMATION_MESSAGE,null);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
    
    public void eliminar(){
        Contacto c= new Contacto(TextId.getText(),TextNombre.getText(),TextApellido.getText());
        for (int i=0; i<id.size();i++) {
            System.out.println(id.get(i));
            String val1= (String) id.get(i);
            String val2= (String) c.getIdentificacion();
            if(val1.equals(val2)){
                System.out.println("Encontrado");
                File file = new File(rut.get(i));
                file.delete();
                String texto = "Proceso exitoso";
                JOptionPane.showMessageDialog(this, texto,"Mensaje",JOptionPane.INFORMATION_MESSAGE,null);
            }else{
                System.out.println("Archivo inexistente");
            }
        }
    }
    
    public void modificar(){
        Contacto c= new Contacto(TextId.getText(),TextNombre.getText(),TextApellido.getText());
        for (int i=0; i<id.size();i++) {
            System.out.println(id.get(i));
            String val1= (String) id.get(i);
            String val2= (String) c.getIdentificacion();
            if(val1.equals(val2)){
                try { 
                    String contenido = "Nombre = " + c.getNombre() + "\n" +
                    "Apellidos = " + c.getApellidos() + "\n" + "Identificacion = " +
                    c.getIdentificacion();
                    BufferedWriter bw = new BufferedWriter(new FileWriter(rut.get(i)));
                    bw.write(contenido);
                    bw.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }
                String texto = "Proceso exitoso";
                JOptionPane.showMessageDialog(this, texto,"Mensaje",JOptionPane.INFORMATION_MESSAGE,null);
            }else{
                System.out.println("Archivo inexistente");
            }
        }
    }
    
    public void buscar(){
        Contacto c= new Contacto(TextId.getText(),TextNombre.getText(),TextApellido.getText());
        for (int i=0; i<id.size();i++) {
            System.out.println(id.get(i));
            String val1= (String) id.get(i);
            String val2= (String) c.getIdentificacion();
            if(val1.equals(val2)){
                try { 
                    String texto="";
                    BufferedReader br = new BufferedReader(new FileReader(rut.get(i)));
                    String lectura= br.readLine();
                    while(lectura != null){
                        texto=texto+lectura+"\n";
                        lectura= br.readLine();
                    }
                    br.close();
                    textBuscar.setText(texto);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }else{
                System.out.println("Archivo inexistente");
            }
        }
    }

        
        
        
